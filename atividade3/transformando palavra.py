nome = input("Digite o seu nome completo: ")

maiusculo = nome.upper()
minusculo = nome.lower()
capitalizado = nome.capitalize()

print(f"Seu nome todo em maiúsculo: {maiusculo}")
print(f"Seu nome todo em minusculo: {minusculo}")
print(f"Seu nome com as iniciais em maiúsculo: {capitalizado}")
